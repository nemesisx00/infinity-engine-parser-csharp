	private static bool DoReadBit(byte value, int bit) => DoReadBit((long)value, bit, 7);
	private static bool DoReadBit(short value, int bit) => DoReadBit((long)value, bit, 15);
	private static bool DoReadBit(ushort value, int bit) => DoReadBit((long)value, bit, 15);
	private static bool DoReadBit(int value, int bit) => DoReadBit((long)value, bit, 31);
	private static bool DoReadBit(uint value, int bit) => DoReadBit((long)value, bit, 31);
	private static bool DoReadBit(long value, int bit, int limit = 63)
	{
		if(ShouldThrow(limit, bit))
			throw new ArgumentOutOfRangeException("bit", "Must be a value from 0 to 63");
		
		var check = 1 << bit;
		return (value & check) == check;
	}
	
	private static sbyte DoReadValue(sbyte value, int bit, int shift = 0) => (sbyte)DoReadValue((long)value, bit, shift, 7);
	private static byte DoReadValue(byte value, int bit, int shift = 0) => (byte)DoReadValue((long)value, bit, shift, 7);
	private static short DoReadValue(short value, int bit, int shift = 0) => (short)DoReadValue((long)value, bit, shift, 15);
	private static ushort DoReadValue(ushort value, int bit, int shift = 0) => (ushort)DoReadValue((long)value, bit, shift, 15);
	private static int DoReadValue(int value, int bit, int shift = 0) => (int)DoReadValue((long)value, bit, shift, 31);
	private static uint DoReadValue(uint value, int bit, int shift = 0) => (uint)DoReadValue((long)value, bit, shift, 31);
	private static long DoReadValue(long value, int bit, int shift = 0, int limit = 63)
	{
		if(ShouldThrow(limit, bit))
			throw new ArgumentOutOfRangeException("bit", "Must be a value from 0 to 31");
		if(ShouldThrow(limit, shift))
			throw new ArgumentOutOfRangeException("shift", "Must be a value from 0 to 30");
		
		return (value >> shift) & (1 << bit) - 1;
	}
	
	private static bool ShouldThrow(int limit, int value) => value < 0 || value > limit;
